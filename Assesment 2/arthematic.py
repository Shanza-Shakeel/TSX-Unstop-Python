# Arithmetic Operators in Python

5 + 3      # Addition
# Output: 8

5 - 3      # Subtraction
# Output: 2

5 * 3      # Multiplication
# Output: 15

5 / 2      # Division (Float result)
# Output: 2.5

5 // 2     # Floor Division (Whole number result)
# Output: 2

5 % 2      # Modulus (Remainder)
# Output: 1

5 ** 3     # Exponentiation (Power)
# Output: 125
